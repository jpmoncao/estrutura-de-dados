'''
6. Fatiamento (Slicing)
Dada a lista animais = ["gato", "cachorro", "coelho", "pássaro", "peixe"], mostre as primeiras três posições, as últimas duas e os elementos nas posições pares.
'''

animais = ["gato", "cachorro", "coelho", "pássaro", "peixe"]

print(animais[:3])
print(animais[-2:])
print(animais[::2])
